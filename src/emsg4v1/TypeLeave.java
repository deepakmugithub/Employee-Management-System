    private void searchid()
    {
    try
        {
            ResultSet rs;
            String query = " select id from typeleave ";
            rs = conn.stmt.executeQuery(query);
          
          while ( rs.next())
              
          {
              Object eno = rs.getString("id");
              cid.addItem(eno.toString());
           
          }
        
        }
        catch(Exception e)
        {
            System.out.println("ERROR IN Displaying ID From Leave..."+e);
        
        }             
    
    }
    
    void insert()
    {
        try
        {
        
            PreparedStatement st;
            
            String query = "insert into typeleave (type,details) values(?,?)";
            
            st = conn.con.prepareStatement(query);
              st.setString(1, type.getText());
              st.setString(2, td.getText() );
           
            int resp = st.executeUpdate();
            //conn.con.close();
            if(resp==1)
            {
                JOptionPane.showMessageDialog(this,"Type leave Added Successfully... ","User", HEIGHT);
                //cid.removeAllItems();
            }
        
        }
        catch(SQLException e)
        {
            System.out.println("Error In Type Insertion.... "+e);
        
        }            
        
    } 
    
    private void displayTable()
    {
         try
        {
            ResultSet rs;
            String query = "select * from typeleave";
            rs = conn.stmt.executeQuery(query);
          
          while ( rs.next())
          {
              model.insertRow(model.getRowCount(), new Object[] {rs.getString("id"),rs.getString("type"),rs.getString("details")});
          
          }
        
        }
        catch(Exception e)
        {
            System.out.println("ERROR IN Displaying Table Type leave..."+e);
        }               
    
    }    
    
    void update()
    {

        try
        {
        
            PreparedStatement st;
            
            String query = "update typeleave set type=?, details=?  where id=?";
            
            st = conn.con.prepareStatement(query);
              st.setString(1, type.getText());
              st.setString(2, td.getText());
             
              st.setString(3, (String) cid.getSelectedItem());
              
            int resp = st.executeUpdate();
            //conn.con.close();
            if(resp==1)
            {
                JOptionPane.showMessageDialog(this,"Type Updated Successfully... ","User", HEIGHT); 
            }
        
        }
        catch(SQLException e)
        {
            System.out.println("Error In Type Updating.... "+e);
        
        }                 
    }    

    void delete()
    {
        try
        {
        
            PreparedStatement st;
            
            String query = "delete from typeleave  where id=?";
            
              st = conn.con.prepareStatement(query);
             
              st.setString(1, cid.getSelectedItem().toString());
              
              int resp = st.executeUpdate();
            
            if(resp==1)
            {
                JOptionPane.showMessageDialog(this,"Deletion Done... ","LEAVE", HEIGHT); 
            }       
        }
        catch(SQLException e)
        {
            System.out.println("Error In type Deletion.... "+e);
        }           
    }
    
    void search()
    {
        if( ! cid.getSelectedItem().equals("") )
        {
        try
        {
          ResultSet rs;
          String query = "select * from typeleave where id = '"+cid.getSelectedItem()+"' ";
          rs = conn.stmt.executeQuery(query);
          
          if( rs.next())
           {
                type.setText(rs.getString("type"));
                
                td.setText(rs.getString("details"));
              
           }
          else
              JOptionPane.showMessageDialog(this,"No Record Found For That Type No. ... ","WRONG TYPE ID ", HEIGHT);
          
        }
        catch(HeadlessException | SQLException e)
        {
            System.out.println("Error In View Type ..."+e);
        }               
        }
    }
    
    void clear()
    {
        cid.setSelectedItem("");
        type.setText("");
        td.setText("");
    }
